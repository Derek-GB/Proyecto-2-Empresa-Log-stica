  public void cargar() {
   DefaultTableModel model = (DefaultTableModel) tbRutas.getModel();
    model.setRowCount(0);

    Iterator<Paquete> i = lista.getPaquetes(); 
    while (i.hasNext()) {
        Paquete paquete = i.next();
        Object data[] = {
            paquete.getCodigo(), 
            paquete.getPeso(), 
            paquete.getDestinatario().getNombre(), 
            paquete.getRemitente().getNombre(),
            paquete.getEstado() ,
            paquete.getDescripcion(),                           
                           
        };
        model.addRow(data);
    }

    tbRutas.setModel(model);
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
    tbRutas.setRowSorter(sorter);
}
