 public void cargar() {
    DefaultTableModel model = (DefaultTableModel) tbEmpleados.getModel();
    model.setRowCount(0);

    Iterator<Empleado> i = lista.getEmpleados(); 
    while (i.hasNext()) {
        Empleado empleado = i.next();
        Object data[] = {
            empleado.getIdentificacion(),  
            empleado.getNombre(),          
            empleado.getFechaNacimiento(), 
            empleado.getCorreo(),          
            empleado.getTelefono(),       
            empleado.getPuesto(),          
            empleado.getSalario()          
        };
        model.addRow(data);
    }

    tbEmpleados.setModel(model);
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
    tbEmpleados.setRowSorter(sorter);
}
